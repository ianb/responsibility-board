// Point values for questions (standard Jeopardy format)
const POINT_VALUES = [200, 400, 600, 800, 1000];

// Parse categories from text format
function parseCategoriesText(text) {
  const categories = [];
  const lines = text
    .split("\n")
    .map((l) => l.trim())
    .filter((l) => l);

  let currentCategory = null;

  // Parse categories and questions
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    // Check if it's a category header
    const categoryMatch = line.match(/^\[(.+)\]$/);
    if (categoryMatch) {
      // Save previous category if it exists
      if (currentCategory) {
        categories.push(currentCategory);
      }
      // Start new category
      currentCategory = {
        category: categoryMatch[1],
        questions: [],
      };
    } else if (currentCategory && line.includes("=")) {
      // Parse question = answer
      const parts = line.split("=").map((p) => p.trim());
      if (parts.length === 2) {
        const questionIndex = currentCategory.questions.length;
        if (questionIndex < POINT_VALUES.length) {
          currentCategory.questions.push({
            question: parts[0],
            answer: parts[1],
            value: POINT_VALUES[questionIndex],
          });
        }
      }
    }
  }

  // Add last category
  if (currentCategory) {
    categories.push(currentCategory);
  }

  return categories;
}

// Parse all categories
const ALL_CATEGORIES = parseCategoriesText(boardsText);

// Cache for random board selections (keyed by board name/session)
const RANDOM_BOARD_CACHE = {};

// Shuffle array using Fisher-Yates algorithm
function shuffleArray(array) {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// Pick 5 random categories
function getRandomCategories(count = 5) {
  const shuffled = shuffleArray(ALL_CATEGORIES);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

// Generate a new random board
function generateRandomBoard() {
  const randomCategories = getRandomCategories(5);
  return {
    name: "Random",
    categories: randomCategories,
  };
}

// Get board names (returns a single "Random" option)
function getBoardNames() {
  return ["Random"];
}

// Get a specific board - generates new random categories if not cached
function getBoard(boardName) {
  // For "Random" board, generate new random set if not cached
  // This ensures same board name returns same categories in a session
  if (!RANDOM_BOARD_CACHE[boardName]) {
    RANDOM_BOARD_CACHE[boardName] = generateRandomBoard();
  }
  return RANDOM_BOARD_CACHE[boardName];
}

// Get 5 random categories as the default board
function getDefaultBoard() {
  // Return cached board or generate new one
  // Cache persists for this page session only, so refresh = new categories
  return getBoard("Random");
}

// Reshuffle categories - clears cache and generates new random board
function reshuffleCategories() {
  // Clear the cache to force new random selection
  delete RANDOM_BOARD_CACHE["Random"];
  // Generate new random board
  return getDefaultBoard();
}
